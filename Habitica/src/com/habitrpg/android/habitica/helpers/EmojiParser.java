    public static String parseEmojis(String text){
        String returnString = text;
        //Pattern to match
        Pattern pattern = Pattern.compile("(:[^:]+:)");
        Matcher matcher = pattern.matcher(text);
        while (matcher.find()) {
            String found = matcher.group();
            if(IEmojiMap.get(found)==null)continue;
            String codestr = IEmojiMap.get(found);
            int hexInt = Integer.parseInt(codestr, 16);
            String replacement = new String(Character.toChars(hexInt));
            returnString = returnString.replace(found, replacement);
        }
        //Returning text
        return returnString;
    }

